public class NumeroMayor {
    public static void main(String[] args) {
        int a=5,b=8,c=10,mayor=0;
        
        if (a>mayor) {
            mayor=a;
        }if (b>mayor) {
            mayor=b;
        }if (c>mayor) {
            mayor=c;
        }
        System.out.println("El numero mayor es: "+mayor);
    }
}
